<h2>7.1. Wprowadzenie</h2>
<p> W zagadnieniu aproksymacji funkcję F(x), określoną tablicą wartości lub o znanej postaci
analitycznej, będziemy aproksymować, czyli zastępować inną funkcją f(x), zwaną funkcją aproksymującą 
lub przybliżeniem funkcji F(x). Przybliżenie powoduje powstanie błędów, zwanych błędami aproksymacji. 
Gdy zbiór, na którym mierzymy błąd aproksymacji, jest zbiorem dyskretnym, wówczas mówimy o aproksymacji punktowej, gdy zaś jest to przedział - aproksymację nazywamy integralną.</p>

<p>Niech $F(x)$ oznacza funkcję, którą chcemy aproksymować, X - pewną przestrzeń unormowaną
(skończenie lub nieskończenie wymiarową), taką że F(x) 0 X, a Xn niech oznacza n-wymiarową
podprzestrzeń liniową przestrzeni X.</p>

